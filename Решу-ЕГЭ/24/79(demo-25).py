# s = '7-99*00-00--88-*00*80*06---7'
f = open("demo_2025_24.txt")
s = f.read()

# Создаем регулярное выражение для корректных выражений
# (?:0|[6-9][0-9]*) - число без ведущих нулей
# (?:[-*]) - операция

